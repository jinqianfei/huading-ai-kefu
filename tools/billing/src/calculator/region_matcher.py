def match_region(store_name: str) -> str:
    """
    从门店名称匹配目的地区域
    
    Args:
        store_name: 门店名称，例如"宫小燕（昆明世纪城店）"
    
    Returns:
        str: 匹配的区域名称，例如"云南全境"、"云南省"等
    """
    if not store_name:
        return "未知区域"
    
    # 从门店名称中提取位置信息（括号内内容）
    location_info = extract_location_from_store_name(store_name)
    
    # 匹配省级区域（从括号内提取的位置信息优先）
    for province, keywords in PROVINCE_KEYWORDS.items():
        for keyword in keywords:
            if keyword in location_info:
                return province
    
    # 尝试匹配清理后的门店名称（无括号版本）
    clean_name = store_name.split("（")[0].split("(")[0].strip()
    
    for province, keywords in PROVINCE_KEYWORDS.items():
        for keyword in keywords:
            if keyword in clean_name:
                return province
    
    return "未匹配区域"


def extract_location_from_store_name(store_name: str) -> str:
    """
    从门店名称中提取位置信息
    
    Args:
        store_name: 门店名称，例如"宫小燕（昆明世纪城店）"
    
    Returns:
        str: 位置信息，例如"昆明世纪城店"
    """
    # 提取括号内容
    if "（" in store_name and "）" in store_name:
        start = store_name.find("（") + 1
        end = store_name.find("）")
        return store_name[start:end]
    elif "(" in store_name and ")" in store_name:
        start = store_name.find("(") + 1
        end = store_name.find(")")
        return store_name[start:end]
    else:
        return store_name


def is_hunan_store(store_name: str) -> bool:
    """
    判断是否为湖南省门店
    
    Args:
        store_name: 门店名称
    
    Returns:
        bool: 是否为湖南省门店
    """
    hunan_keywords = ["湖南", "长沙", "株洲", "湘潭", "衡阳", "岳阳", "常德", "张家界", "益阳", "郴州", "永州", "怀化", "娄底", "湘西", "湘江"]
    
    # 如果门店名称中包含湖南省的城市关键词，且不是以其他省份开头的，则认为是湖南省门店
    for keyword in hunan_keywords:
        if keyword in store_name:
            # 排除其他省份的同名城市（如贵州的长沙）
            other_provinces = ["贵州", "云南", "江西", "四川", "重庆", "广东", "广西"]
            for province in other_provinces:
                if province + keyword in store_name:
                    return False
            return True
    
    return False


def get_all_regions() -> list:
    """
    获取所有可用的区域列表
    
    Returns:
        list: 区域列表
    """
    regions = set()
    
    # 添加省份
    for province in PROVINCE_KEYWORDS.keys():
        regions.add(province)
    
    # 添加城市
    for city in CITY_KEYWORDS.keys():
        regions.add(city)
    
    # 添加大区别名
    for alias in REGION_ALIASES.keys():
        regions.add(alias)
    
    return sorted(list(regions))


def get_province_by_city(city: str) -> str:
    """
    根据城市名获取所属省份
    
    Args:
        city: 城市名称
    
    Returns:
        str: 省份名称，如果未找到则返回空字符串
    """
    # 去除市/县等后缀
    clean_city = city.replace("市", "").replace("县", "").replace("区", "")
    
    # 先在城市关键词中查找
    for province, keywords in PROVINCE_KEYWORDS.items():
        if city in keywords or clean_city in keywords:
            return province
    
    return ""